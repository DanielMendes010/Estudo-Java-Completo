package com.javaTutorial;

public class IfAndElse {
	public static void main(String[] args) {
		int x = 20, y = 18;
		
		if (x > y) {
			System.out.println("Variable x is greater than y!");
		}
		else if (x == y) {
			System.out.println("Variable x is same that y!");
		}
		else {
			System.out.println("Variable x is no greater than y!");
		}
	}

}
